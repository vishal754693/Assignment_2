# Sum of Integers from 1 to 50 Using a Loop

# Step 1: Initialize a variable to store the sum
total = 0

# Step 2: Use a for loop to iterate from 1 to 50 (inclusive)
for number in range(1, 51):
    total += number  # Add current number to the total

# Step 3: Display the final sum
print("The sum of integers from 1 to 50 is:", total)
